    private void ucitajPodatke() {
        DefaultListModel<Osoba> m = new DefaultListModel<>();

        // 1. način
        List<Osoba> lista = obrada.getPodaci();
        for(int i=0;i<lista.size();i++){
            m.addElement(lista.get(i));
        }
//2. način
//        for(Smjer s: lista){
//            m.addElement(s);
//        }
        //obrada.getPodaci().forEach(s -> m.addElement(s));

        lstPodaci.setModel(m);
    }

    private void ocistiPolja() {
        txtIme.setText("");
        txtPrezime.setText("");
        txtBrojTelefona.setText("");
        txtOib.setText("");
    }

    private void postaviVrijednostiUEntitet() {
            entitet.setIme(txtIme.getText());
            entitet.setPrezime(txtPrezime.getText());
            entitet.setBroj_tel(txtBrojTelefona.getText());
            entitet.setOib(txtOib.getText());
            
            obrada.setEntitet(entitet);
    }
